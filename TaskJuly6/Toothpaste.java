class Toothpaste {
    String brand;
    String flavor;
    int volume;
    boolean containsFluoride;
    boolean isWhitening;
    boolean isSensitive;
    String packaging;
    double price;
    
    Toothpaste(String brand, String flavor, int volume, boolean containsFluoride, boolean isWhitening, boolean isSensitive, String packaging, double price) {
        this.brand = brand;
        this.flavor = flavor;
        this.volume = volume;
        this.containsFluoride = containsFluoride;
        this.isWhitening = isWhitening;
        this.isSensitive = isSensitive;
        this.packaging = packaging;
        this.price = price;
    }
	}
    