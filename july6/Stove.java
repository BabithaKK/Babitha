class Stove {
    String brand;
    String model;
    int numberOfBurners;
    String color;
    boolean hasOven;
    boolean hasGrill;
    boolean isElectric;
    double price;
    
    Stove(String brand, String model, int numberOfBurners, String color, boolean hasOven, boolean hasGrill, boolean isElectric, double price) {
        this.brand = brand;
        this.model = model;
        this.numberOfBurners = numberOfBurners;
        this.color = color;
        this.hasOven = hasOven;
        this.hasGrill = hasGrill;
        this.isElectric = isElectric;
        this.price = price;

   
    }
}